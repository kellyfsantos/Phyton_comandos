# Operações matemáticas e aritméticas

"""
Soma: +
Subtração: -
Multiplicação: *
Divisão: /
Divisão inteira: //
Resto da divisão: %
Potência: **

"""
numero1 = 10
numero2 = 20

print(numero1 + numero2) #a soma deu 200 e não 30 : necessário converter a variável de string para inteira
print(numero1 - numero2)
print(numero1 * numero2)
print(numero1 / numero2)
print(numero1 // numero2)
print(20 % 3)
print(2 ** 3)

